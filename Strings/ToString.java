public class ToString {
    public static void main(String[] args) {
        //toString() function is used to convert a object datatype to string.
        Integer i = 1947;
        i.toString(0);
        System.out.println(i);

        StringBuilder sb = new StringBuilder("ALLAH");
        sb.toString();
        System.out.println(sb);

        Float f = 5.7f;
        f.toString();
        System.out.println(f);

    }
}
